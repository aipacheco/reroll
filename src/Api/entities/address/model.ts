import { Schema, model } from "mongoose"

const AddressSchema = new Schema(
  {
    owner: {
      type: Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
    name: {
      type: String,
      required: true,
    },
    lastName: {
      type: String,
      required: true,
    },
    streetAddress : {
      type: String,
      required: true,
    },
    city: {
      type: String,
      required: true,
    },
    province: {
      type: String,
      required: true,
    },
    cp: {
      type: Number,
      required: true,
    },
  },


)

const Address = model("Address", AddressSchema)

export default Address
